#include <unistd.h>

void	ft_is_negative(int n)
{
	char	answer;

	if (n >= 0)
	{
		answer = 'P';
		write(1, &answer, 1);
	}
	else
	{
		answer = 'N';
		write(1, &answer, 1);
	}
}
